<h2 style="color:#4f46e5;">About This Application</h2>

<p style="line-height:1.7;color:#444;">
The Faculty Leave Management System is a application designed to automate the process of applying, approving, and managing faculty leave requests. This system eliminates manual paperwork and provides a centralized platform to improve efficiency, transparency, and accuracy in leave processing.
</p>

<h3 style="margin-top:20px;">Application Workflow</h3>

<ul style="line-height:1.8;color:#333;">
<li>Faculty logs into the system.</li>
<li>Faculty submits leave requests online.</li>
<li>Administrator reviews the requests.</li>
<li>Admin approves or rejects leave.</li>
<li>Status is updated instantly.</li>
<li>Faculty can track leave history.</li>
<li>Admin can generate reports.</li>
</ul>

<h3 style="margin-top:20px;">Key Features</h3>

<ul style="line-height:1.8;color:#333;">
<li>Secure login for Admin and Faculty</li>
<li>Online leave application</li>
<li>Leave approval or rejection</li>
<li>Leave status tracking</li>
<li>Centralized MySQL database</li>
<li>PDF report generation</li>
</ul>

